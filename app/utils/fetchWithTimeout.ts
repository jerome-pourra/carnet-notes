const TIMEOUT_DURATION = 5000;

export const fetchWithTimeout = async (url: string, options: RequestInit = {}, timeout = TIMEOUT_DURATION) => {
  const controller = new AbortController();
  const id = setTimeout(() => controller.abort(), timeout);

  try {
    const response = await fetch(url, {
      ...options,
      signal: controller.signal
    });
    clearTimeout(id);
    return response;
  } catch (error) {
    clearTimeout(id);
    throw error;
  }
};